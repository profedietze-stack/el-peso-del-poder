export function getEventStickers(tag) {
  const pool = STICKER_MAP[tag] || ['⭐','🌟','💫','✨','🎯','📌','🔶','🔷','🟡','🟠'];
  const count = 3 + Math.floor(Math.random() * 3);
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

export function buildStickerHTML(stickers) {
  const positions = [
    {top:'8%',  left:'6%'},  {top:'8%',  right:'6%'}, {top:'8%',  left:'50%'},
    {top:'50%', left:'8%'},  {top:'50%', right:'8%'},  {bottom:'12%', left:'10%'},
  ];
  return stickers.map((s, i) => {
    const pos = positions[i % positions.length];
    const rotation = (Math.random() * 30 - 15).toFixed(1);
    const delay = (i * 0.08).toFixed(2);
    const styleEntries = Object.entries(pos).map(([k, v]) => `${k}:${v}`).join(';');
    return `<span class="ev-sticker" style="--sr:${rotation}deg;${styleEntries};animation-delay:${delay}s">${s}</span>`;
  }).join('');
}
